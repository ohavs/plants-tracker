import { writeFileSync, readFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

// Parse a simple .env file and populate process.env (only for missing keys)
function loadEnvFile(path) {
  try {
    const lines = readFileSync(path, 'utf8').split('\n');
    for (const line of lines) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#')) continue;
      const eqIdx = trimmed.indexOf('=');
      if (eqIdx < 0) continue;
      const key = trimmed.slice(0, eqIdx).trim();
      const val = trimmed.slice(eqIdx + 1).trim().replace(/^["']|["']$/g, '');
      if (key && !(key in process.env)) process.env[key] = val;
    }
  } catch {}
}

loadEnvFile(resolve(root, '.env.local'));
loadEnvFile(resolve(root, '.env'));

const config = {
  apiKey:            process.env.NEXT_PUBLIC_FIREBASE_API_KEY            || '',
  authDomain:        process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN        || '',
  projectId:         process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID         || '',
  storageBucket:     process.env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET     || '',
  messagingSenderId: process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID || '',
  appId:             process.env.NEXT_PUBLIC_FIREBASE_APP_ID             || '',
};

const vapidKey = process.env.NEXT_PUBLIC_FIREBASE_VAPID_KEY || '';

if (!config.projectId) {
  console.warn('[generate-firebase-sw] WARNING: Firebase env vars not set — __env.js will have empty values.');
}

const content = `// Auto-generated by scripts/generate-firebase-sw.mjs — do not edit manually
self.__FIREBASE_CONFIG__ = ${JSON.stringify(config, null, 2)};
self.__FIREBASE_VAPID_KEY__ = ${JSON.stringify(vapidKey)};
`;

writeFileSync(resolve(root, 'public/__env.js'), content);
console.log('[generate-firebase-sw] ✓ public/__env.js written');
